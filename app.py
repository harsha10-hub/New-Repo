# Simple Python 3 Example: List Comprehension
fruits = ['Banana', 'Apple', 'Lime']
loud_fruits = [fruit.upper() for fruit in fruits]
print(loud_fruits)  # Output: ['BANANA', 'APPLE', 'LIME']

# Basic Function
def greet(name):
    return f"Hello, {name}!"

print(greet("World")) # Output: Hello, World!
